create or replace function public.v27_customer_id_by_phone(p_phone text)
returns uuid language sql stable security definer set search_path=public as $$
  select id from public.customer_profiles
  where regexp_replace(phone,'\D','','g')=regexp_replace(p_phone,'\D','','g')
  order by archived asc, updated_at desc limit 1
$$;

create or replace function public.v27_queue_experience_on_completion()
returns trigger language plpgsql security definer set search_path=public as $$
declare v_customer uuid;
begin
  if new.status='completed' and coalesce(old.status,'') <> 'completed' then
    v_customer:=public.v27_customer_id_by_phone(new.customer_phone);
    if v_customer is not null then
      insert into public.customer_timeline(customer_id,booking_id,event_type,title,details)
      values(v_customer,new.id,'attendance_completed','Atendimento concluído',jsonb_build_object('services',new.service_name,'value',coalesce(new.service_price,0)+coalesce(new.products_price,0)));
    end if;
    if nullif(trim(coalesce(new.customer_email,'')),'') is not null then
      insert into public.experience_requests(booking_id,customer_id,customer_name,customer_email,scheduled_for)
      values(new.id,v_customer,new.customer_name,lower(trim(new.customer_email)),now()+interval '2 hours')
      on conflict(booking_id) do nothing;
    end if;
  end if;
  return new;
end $$;

drop trigger if exists bookings_v27_experience on public.bookings;
create trigger bookings_v27_experience after update of status on public.bookings
for each row execute function public.v27_queue_experience_on_completion();

create or replace function public.get_experience_context(p_token uuid)
returns jsonb language plpgsql security definer set search_path=public as $$
declare r public.experience_requests; b public.bookings;
begin
  select * into r from public.experience_requests where token=p_token;
  if r.id is null then return jsonb_build_object('ok',false,'error','Link inválido.'); end if;
  select * into b from public.bookings where id=r.booking_id;
  update public.experience_requests set opened_at=coalesce(opened_at,now()),updated_at=now() where id=r.id;
  return jsonb_build_object('ok',true,'first_name',split_part(trim(r.customer_name),' ',1),'booking_date',b.booking_date,'service_name',b.service_name,'answered',r.answered_at is not null,'answer',r.answer);
end $$;

create or replace function public.submit_experience_response(p_token uuid,p_answer text,p_feedback text default null)
returns jsonb language plpgsql security definer set search_path=public as $$
declare r public.experience_requests;
begin
  if p_answer not in ('satisfied','suggestion') then raise exception 'Resposta inválida.'; end if;
  select * into r from public.experience_requests where token=p_token for update;
  if r.id is null then return jsonb_build_object('ok',false,'error','Link inválido.'); end if;
  if r.answered_at is not null then return jsonb_build_object('ok',true,'already_answered',true,'answer',r.answer); end if;
  if p_answer='suggestion' and char_length(trim(coalesce(p_feedback,'')))<3 then raise exception 'Conte brevemente o que podemos melhorar.'; end if;
  update public.experience_requests set answer=p_answer,feedback=nullif(trim(p_feedback),''),answered_at=now(),status='answered',updated_at=now() where id=r.id;
  if r.customer_id is not null then
    update public.customer_profiles set satisfaction_status=p_answer,satisfaction_updated_at=now(),updated_at=now() where id=r.customer_id;
    insert into public.customer_timeline(customer_id,booking_id,event_type,title,details)
    values(r.customer_id,r.booking_id,case when p_answer='satisfied' then 'satisfaction_positive' else 'feedback_private' end,
      case when p_answer='satisfied' then 'Cliente informou que ficou satisfeito' else 'Cliente enviou uma sugestão privada' end,
      jsonb_build_object('feedback',nullif(trim(p_feedback),'')));
  end if;
  return jsonb_build_object('ok',true,'answer',p_answer);
end $$;

create or replace function public.mark_experience_google_click(p_token uuid)
returns boolean language plpgsql security definer set search_path=public as $$
declare v_customer uuid; v_booking uuid;
begin
  update public.experience_requests set google_clicked_at=coalesce(google_clicked_at,now()),updated_at=now()
  where token=p_token returning customer_id,booking_id into v_customer,v_booking;
  if v_customer is not null then
    update public.customer_profiles set google_reviewed=true,google_reviewed_at=coalesce(google_reviewed_at,now()),updated_at=now() where id=v_customer;
    insert into public.customer_timeline(customer_id,booking_id,event_type,title)
    values(v_customer,v_booking,'google_review_click','Cliente abriu a página de avaliação do Google');
  end if;
  return found;
end $$;


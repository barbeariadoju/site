create or replace function public.admin_customer_insights_v27()
returns table(
 customer_id uuid,name text,phone text,email text,completed_visits bigint,total_spent numeric,ticket_average numeric,
 last_visit date,days_inactive integer,average_interval numeric,next_expected date,loyalty_points integer,rewards_available integer,
 ju_score integer,segment text,google_reviewed boolean,satisfaction_status text
) language sql stable security definer set search_path=public as $$
with completed as (
 select c.id customer_id,b.booking_date,(coalesce(b.service_price,0)+coalesce(b.products_price,0))::numeric amount,
 lag(b.booking_date) over(partition by c.id order by b.booking_date,b.start_time) prev_date
 from public.customer_profiles c left join public.bookings b
 on regexp_replace(b.customer_phone,'\D','','g')=regexp_replace(c.phone,'\D','','g') and b.status='completed'
 where c.archived=false
), agg as (
 select customer_id,count(booking_date) completed_visits,coalesce(sum(amount),0) total_spent,max(booking_date) last_visit,
 avg((booking_date-prev_date)) filter(where prev_date is not null) average_interval from completed group by customer_id
)
select c.id,c.name,c.phone,c.email,coalesce(a.completed_visits,0),coalesce(a.total_spent,0),
 case when coalesce(a.completed_visits,0)>0 then round(a.total_spent/a.completed_visits,2) else 0 end,
 a.last_visit,case when a.last_visit is null then null else current_date-a.last_visit end,
 round(coalesce(a.average_interval,c.return_interval_days,30),1),
 case when a.last_visit is null then null else a.last_visit+round(coalesce(a.average_interval,c.return_interval_days,30))::int end,
 coalesce(l.points,0),coalesce(l.rewards_available,0),
 least(100,greatest(0,(coalesce(a.completed_visits,0)*6 + least(25,coalesce(a.total_spent,0)/40) + case when c.google_reviewed then 12 else 0 end + case when coalesce(l.rewards_available,0)>0 then 8 else 0 end - case when a.last_visit is not null and current_date-a.last_visit>60 then 18 else 0 end)::int)),
 case when coalesce(a.total_spent,0)>=1200 or coalesce(a.completed_visits,0)>=18 then 'Platinum'
      when coalesce(a.total_spent,0)>=700 or coalesce(a.completed_visits,0)>=10 then 'Gold'
      when coalesce(a.total_spent,0)>=300 or coalesce(a.completed_visits,0)>=5 then 'Silver' else 'Bronze' end,
 c.google_reviewed,c.satisfaction_status
from public.customer_profiles c left join agg a on a.customer_id=c.id left join public.loyalty_accounts l on l.customer_id=c.id
where c.archived=false order by coalesce(a.last_visit,date '1900-01-01') desc;
$$;
grant execute on function public.admin_customer_insights_v27() to authenticated,service_role;


create table if not exists public.customer_timeline (
  id bigint generated by default as identity primary key,
  customer_id uuid references public.customer_profiles(id) on delete cascade,
  booking_id uuid references public.bookings(id) on delete set null,
  event_type text not null,
  title text not null,
  details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists customer_timeline_customer_date_idx
  on public.customer_timeline(customer_id, created_at desc);

create table if not exists public.experience_requests (
  id uuid primary key default gen_random_uuid(),
  booking_id uuid not null references public.bookings(id) on delete cascade,
  customer_id uuid references public.customer_profiles(id) on delete set null,
  token uuid not null default gen_random_uuid() unique,
  customer_name text not null,
  customer_email text not null,
  scheduled_for timestamptz not null default (now() + interval '2 hours'),
  sent_at timestamptz,
  opened_at timestamptz,
  answered_at timestamptz,
  answer text check (answer is null or answer in ('satisfied','suggestion')),
  feedback text,
  google_clicked_at timestamptz,
  status text not null default 'pending' check (status in ('pending','sent','answered','failed','cancelled')),
  attempts integer not null default 0,
  last_error text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique(booking_id)
);

create index if not exists experience_requests_due_idx
  on public.experience_requests(status, scheduled_for)
  where status='pending';

alter table public.customer_timeline enable row level security;
alter table public.experience_requests enable row level security;

